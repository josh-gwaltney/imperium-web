////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import Component from '../../engine/component';

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
class PositionComponent extends Component{
  constructor (entityId, state) {
    super(entityId, state);

    this._x = state.x;
    this._y = state.y;
    this._z = state.z;
  };

  get x(){
    return this._x;
  }

  get y(){
    return this._y;
  }

  get z(){
    return this._z;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////

}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default PositionComponent;