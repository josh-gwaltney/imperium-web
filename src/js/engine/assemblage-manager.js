class AssemblageManager {
  constructor(){
    this._templates = {};
    this._init();
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  findTemplate(id){
    if(this._templates[id]){
      return this._templates[id];
    }
    return false
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////
  _init(){

  }

  _addTemplate(template){
    let id = template.id;
    let components = template.components;
    if(!this._templates[id]){
        this._templates[id] = components;
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default AssemblageManager;